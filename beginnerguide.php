<li>Go to the Grand Exchange in-game or use a third-party website(Like this one) that tracks the Grand Exchange or player given transactions.</li>
<li>Look up the item you are interested in flipping by typing its name into the search bar.</li>
<li>The results page will show you the current buy and sell prices of the item. The buy price is the price that players are offering to buy the item,
  and the sell price is the price that players are offering to sell the item.</li>
<li>To find the margin, subtract the buy price from the sell price. For example,
   if the buy price is 100 coins and the sell price is 200 coins, the margin would be 100 coins (200 - 100 = 100).</br>
  </li>
</br>
It's important to note that the margins of items in the Grand Exchange can change quickly due to supply and demand, so it's important to monitor the market regularly if you plan on flipping items for profit.</br>
 Additionally, some items may have buy limits, which restrict the number of items that a player can purchase within a certain amount of time.</br>
  Be aware of these limits when planning your flipping strategy.
